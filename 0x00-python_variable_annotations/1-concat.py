#!/usr/bin/env python3
"""
This is the "1-concat" module.
The 1-concat module function: concat(str1: str, str2: str) -> str.
"""


def concat(str1: str, str2: str) -> str:
    """
    concat: concatenate 2 strings

    Args:
        str1 (str): first string
        str2 (str): second string
    Return:
        string
    """
    return str1 + str2
