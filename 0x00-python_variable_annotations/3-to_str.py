#!/usr/bin/env python3

"""
This is the "3-to_str" module.
The 3-to_str module function: to_str(n: float) -> str.
"""


def to_str(n: float) -> str:
    """
    to_str: returns the string representation of the float.

    Args:
        n (float): a float
    Return:
        string
    """
    return str(n)
