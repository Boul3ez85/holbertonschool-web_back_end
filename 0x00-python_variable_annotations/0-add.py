"""
This is the "0-add" module.
The 0-add module function: add(a: float, b: float) -> float.
"""


def add(a: float, b: float) -> float:
    """
    add: add 2 floats

    Args:
        a (float): first float
        b (float): second float
    Return:
        float
    """
    return a + b
