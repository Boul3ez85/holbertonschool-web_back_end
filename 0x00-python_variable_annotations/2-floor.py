#!/usr/bin/env python3
"""
This is the "2-floor" module.
The 2-floor module function: floor(n: float) -> int.
"""


import math


def floor(n: float) -> int:
    """
    floor: calculate the floor of a given float

    Args:
        n (float): a float
    Return:
        integer
    """
    return math.floor(n)
